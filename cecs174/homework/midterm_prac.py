def luckyXs(string):
    count = 0
    for i in range(len(string)):
        if string[i] == 'X' and (i % 2) == 0:
            count += 1
    return count


def countWords(string):
    count = 0
    for i in range(len(string)):
        if string[i] == " ":
            count = count + 1
    return count + 1

